WITH booking_price AS(
	SELECT
		b.id,
		SUM(r.price) + SUM(COALESCE(s.price, 0)) price
	FROM
		booking b
		INNER JOIN room r ON b.room_id = r.id
		LEFT JOIN booking_service bs ON b.id = bs.booking_id
		LEFT JOIN service s ON bs.service_id = s.id
	GROUP BY
		b.id
)
UPDATE
	booking b
SET
	cost = (EXTRACT(DAY FROM(b.check_out - b.check_in)) + 1) * bp.price
FROM
	booking_price bp
WHERE
	b.id = bp.id;
